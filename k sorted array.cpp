class Solution {
public:
	string isKSortedArray(int arr[], int n, int k)
	{
		//code here.
		int temp[n];
		for (int i = 0; i < n; i++)
			temp[i] = arr[i];
		sort(temp, temp + n);
		for (int i = 0; i < n; i++) {
			int index = lower_bound(temp, temp + n, arr[i]) - temp;
			index = abs(index - i);
			//cout << index << endl;
			if (index > k)
				return "No";
		}
		return "Yes";
	}
};

