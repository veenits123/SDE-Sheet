class Solution {
public:
	int totalHammingDistance(vector<int>& nums) {
		int ans = 0;
		for (int i = 0; i < 32; i++) {
			int cnt = 0;
			for (int j = 0; j < nums.size(); j++) {
				if (nums[j] & (1 << i))
					cnt++;//no. of set bits at i'th bit of every elements;
				//(nums.size() - cnt) = no. of 0 bits at i'th bit of every elements;
			}
			ans += cnt * (nums.size() - cnt);
		}
		return ans;
	}
};

